export class WeaponInstance {
    charid: number;
    name: string;
    weapon: number;
    weaponInstId: number;
    weaponname: string;

constructor (charId, name, weapon, weaponInstId, weaponname){
    this.charid = charId;
    this.name = name;
    this.weapon = weapon;
    this.weaponInstId = weaponInstId;
    this.weaponname = weaponname;
}


}

